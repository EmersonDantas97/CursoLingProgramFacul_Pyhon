# Coleções para armazenar dados. 
texto = "Explorando a diversidade de linguagens de programação com Python."

print(f"Tamanho do texto: {len(texto)}.")
print(f"Python in texto: {'Python' in texto}.") # Verificando se a palavra Python existe. Lembrando que é case sensitive.
print(f"Quantidade de 'e' no texto: {texto.count('e')}.") 
print(f"As 5 primeiras letras são: {texto[:5]}.") 
print(f"2º caracter da variavel texto: {texto[1]}.") # Lembrando que o array comeca no 0.

 