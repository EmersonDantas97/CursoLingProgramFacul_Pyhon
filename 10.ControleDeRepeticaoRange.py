# Aprendendo a utilizar os controles de repições.

for x in range(5): # Lembre-se que comeca no 0 e termina no 4.
    print(f'O intervalo é: {x}')

for y in range(2, 7):
    print(f'O intervalo é: {y}')

for z in range(1, 11, 2): # Range vai do 1 ao 11 e vai de 2 em 2. 
    print(f'O intervalo é: {z}')
