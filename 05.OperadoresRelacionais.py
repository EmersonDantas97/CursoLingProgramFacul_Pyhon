# Operadores relacionais, para fazer comparacoes

# <         Estritamente menor que 
# <=        Menor ou igual que
# >         Estritamente maior que
# >=        Maior ou igual que
# ==        Igual
# !=        Diferente
# is        Identidade do objeto
# is not    Negacao da identidade do objeto

# Teste logico 1
print(1>2)

# Teste logico 2
print(1<2)

# Teste logico 3
aluno_1 = 'Marcio'
aluno_2 = 'Jose'

print(aluno_1==aluno_2)

# Teste logico 4
print(aluno_1 is aluno_2)

# Teste logico 5
print(aluno_1 is not aluno_2)