# Variaveis, tipos e utilizacao do comando type.

x = 10 # Variavel do tipo inteiro.
nome = 'aluno' # Variavel do tipo string.
nota = 8.75 # Variavel do tipo float. Usa ponto inves de virgula. 
fez_inscricao = True # Variavel do tipo bool.

#print(type(X)) # Python e case sensitive, comando dara erro, por que declaramos a variavel em minusculo.
print(type(x))
print(type(nome))
print(type(nota))
print(type(fez_inscricao))